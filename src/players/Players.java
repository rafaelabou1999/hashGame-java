package players;

public class Players {
	int positionLine;
	int positionCol;
	
	
	public void setPositionCol(int col) {
		this.positionCol = col;
	}
	
	public void setPositionLine(int line) {
		this.positionLine = line;
	}
	
	public int getPositionCol() {
		return positionCol;
	}
	
	public int getPositionLine() {
		return positionLine;
	}
	
	
}
